    public void carregar(String valor){
        String [] titulos= {"id","nome","endereço","telefone","email"};
        String []registros = new String[5];
        String sql = "SELECT * FROM alunos WHERE nome LIKE '%"+valor+"%'";
        
        model = new DefaultTableModel(null,titulos);
        
        ConectaMysql con = new ConectaMysql();
        Connection conectar = con.getConnection();
        try{
            java.sql.Statement ps = conectar.createStatement();
            ResultSet rs = ps.executeQuery(sql);
            while (rs.next()){
                
                registros[0]=rs.getString("id");
                registros[1]=rs.getString("nome");
                registros[2]=rs.getString("endereço");
                registros[3]=rs.getString("telefone");
                registros[4]=rs.getString("email");
                
                model.addRow(registros);
            }
                tableDados.setModel(model);
                        
            }catch(SQLException ex){
                JOptionPane.showMessageDialog(null, ex);
           }
    }  
        
