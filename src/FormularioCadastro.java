    private void bt_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_salvarActionPerformed
        
        
        if(checarDados() != false){
            ConectaMysql con = new ConectaMysql();
            
            Connection conecta = con.getConnection();
            String sql = "INSERT INTO alunos(nome, endereço, telefone, email, senha) VALUES (?,?,?,?,?)";
            try {
                PreparedStatement st = conecta.prepareStatement(sql);
                st.setString(1,txt_nome.getText());
                st.setString(2, txt_endereco.getText());
                st.setString(3, txt_telefone.getText());
                st.setString(4,txt_email.getText());
                st.setString(5,password.getText());
                st.executeUpdate();
                conecta.commit();
                
            } catch (SQLException ex) {
                Logger.getLogger(FormularioCadastro.class.getName()).log(Level.SEVERE, null, ex);
                try {
                    conecta.rollback();
                } catch (SQLException ex1) {
                    Logger.getLogger(FormularioCadastro.class.getName()).log(Level.SEVERE, null, ex1);
                }
            }
            
            
        }
    }//GEN-LAST:event_bt_salvarActionPerformed

